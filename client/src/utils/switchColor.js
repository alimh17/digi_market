export const switchColor = (color) => {
  switch (color) {
    case "black":
      return "مشکی";
    case "white":
      return "سفید";
    case "green":
      return "سبز";
    case "purple":
      return "بنفش";
    case "blue":
      return "آبی";
    case "red":
      return "قرمز";
    case "pink":
      return "صورتی";
    case "gray":
      return "خاکستری";
    case "gold":
      return "طلایی";
    default:
      return "مشکی";
  }
};
